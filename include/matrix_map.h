#pragma once
#include <Arduino.h>

// Структура для описания одной ячейки матрицы
struct CellMap {
    uint8_t slaveId; // 0..3
    uint8_t mask;    // битовая маска 0b0001, 0b0010 и т.д.
};

/**
 * Глобальная карта 4x4 (16 ячеек).
 * Индекс в массиве = Row * 4 + Col
 * Расположение:
 * [ 0] [ 1] [ 2] [ 3]  (Ряд 0)
 * [ 4] [ 5] [ 6] [ 7]  (Ряд 1)
 * [ 8] [ 9] [10] [11]  (Ряд 2)
 * [12] [13] [14] [15]  (Ряд 3)
 */
const CellMap MATRIX_MAP[16] = {
    // Ряд 0: Slave 0 (0,1), Slave 1 (0,1)
    {0, 0b0001}, {0, 0b0010}, {1, 0b0001}, {1, 0b0010},
    // Ряд 1: Slave 0 (2,3), Slave 1 (2,3)
    {0, 0b0100}, {0, 0b1000}, {1, 0b0100}, {1, 0b1000},
    // Ряд 2: Slave 2 (0,1), Slave 3 (0,1)
    {2, 0b0001}, {2, 0b0010}, {3, 0b0001}, {3, 0b0010},
    // Ряд 3: Slave 2 (2,3), Slave 3 (2,3)
    {2, 0b0100}, {2, 0b1000}, {3, 0b0100}, {3, 0b1000}
};